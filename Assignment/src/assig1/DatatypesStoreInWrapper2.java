package assig1;

public class DatatypesStoreInWrapper2 {
	public static void main(String[] args){

		int a = 10;
		Integer obj1 = a;
		System.out.println(obj1);
		
		short s = 20;
		Short obj2 = s;
		System.out.println(obj2);

		long l = 923759842398l;
		Long obj3 = l;
		System.out.println(obj3);

		float f = 33.32f;
		Float obj4 = f;
		System.out.println(obj4);

		double d = 21.55d;
		Double obj5 = d;
		System.out.println(obj5);

		char c = 'G';
		Character obj6 = c;
		System.out.println(obj6);

		boolean b = true;
		Boolean obj7 = b;
		System.out.println(obj7);

		byte b1 = 21;
		Byte obj8 = b1;
		System.out.println(obj8);	
			
		
		}
}
